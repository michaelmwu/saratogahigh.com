<h1>SaratogaHigh.com Privacy Policy</h1>

<div style="width: 600px">
<p>This document is meant to clarify our policies on how we collect personal information and what we do with it. It is our general policy to keep your personal information private, and to expose your public information only to <a href="help/validation.php">verified</a> SaratogaHigh.com users.</p>

<h2>What information do we collect?</h2>

<h3>Information that we receive from you</h3>
<p>During the registration process, you will be asked to enter your name, your email address, and (if applicable) your student ID number.</p>
<p>If you use the Directory feature, you may choose to enter your schedule information, as well as additional contact information (for example, email address and phone number) to share with other users.</p>
<p>SaratogaHigh.com may hold surveys, collect information, or conduct registration on behalf of, and as a service to, other organizations. These organizations may ask for your personal information.</p>

<h3>Information we receive from the school</h3>
<p>If you are a student, the school provides us with your name, grade, student ID number, and street address. This information is distributed in compliance with the <a href="http://www.ed.gov/offices/OII/fpco/ferpa/">Family Education Rights and Privacy Act</a> (FERPA).</p>

<h2>How is your personal information used?</h2>
<p>During the registration process, your name, and student ID number, if you are a student, are checked against the information we received from the school, in order to verify your identity.</p>
<p>A distinction should be made between the email address you enter during registration and the email address you may choose to enter in the SaratogaHigh.com Directory.</p>
<ol>
<p>Your registration email address will only be used for occasional notices and communication on official SaratogaHigh.com business. We will not distribute your registration email address to third parties, nor will it be visible to other users.</p>
<p>Your Directory email address will be displayed to verified SaratogaHigh.com users, under the terms below.</p>
</ol>
<p>Only verified SaratogaHigh.com users have access to Directory information (your schedule or contact information, if you choose to use it).</p>
<p>We may use your street address to send you mailings and notices, either via USPS or via <a href="/hedwig/">Hedwig</a>, our personal delivery service. As such, your street address may be given to our couriers or to a delivery service, such as USPS.</p>
<p>You may be able to participate in surveys, register for events or fill out forms on SaratogaHigh.com. SaratogaHigh.com provides this capability as an service to other organizations. These forms may ask for your personal information. When you fill out such a form, we will notify you what organization(s) are requesting your information.</p>
<p>In the event of a criminal investigation, we may disclose information to the appropriate authorities in response to a subpoena, warrant, or similar legal process.</p>

<h2>How do we keep your personal information safe?</h2>
<p>Our <a href="/help/validation.php">user verification process</a> ensures that users are who they say they are. Your personal information (e.g. class schedule and public contact information) are only visible to verified users.</p>
<p>Access to database records (such as your address and registration email address) is limited to high-level staff members only.</p>
<p>Your password is encoded by <a href="http://userpages.umbc.edu/~mabzug1/cs/md5/md5.html">MD5</a> hashing. What this means is that it is, in practice, impossible for anyone, even administrators, to find your password.</p>

<h2>What are your privacy choices?</h2>
<p>At any time, you can view or change your registration email address by clicking on Edit Account Info from our home page.</p>
<p>You may view or modify your Directory information (contact information which is displayed to other users) by clicking on Directory and then clicking on Edit Directory Info.</p>
<p>Should these privacy policies change, we will attempt to notify you via email.</p>

</div>

